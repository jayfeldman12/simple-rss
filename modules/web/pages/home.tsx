import type {NextPage} from 'next';
import Head from 'next/head';
import {useEffect, useState} from 'react';
import Row from 'react-bootstrap/Row';
import Button from 'react-bootstrap/Button';
import {useFeeds} from './useFeeds';
import FeedCard from './homeComponents/feedCard';
import SubmitButton from '../common/SubmitButton';

const Home: NextPage = () => {
  const {
    errorMessage,
    isFetching,
    items,
    markAllRead,
    onItemClick,
    setFetchAll,
    setPauseQueries,
    setUsername,
    showFetchAll,
    showMarkAllRead,
    unreadCount,
  } = useFeeds();
  const [windowHeight, setWindowHeight] = useState<number | string>('100rem');

  useEffect(() => {
    if (typeof window !== 'undefined') setWindowHeight(window.innerHeight);
  }, []);

  return (
    <div>
      <Head>
        <title>{`${unreadCount ? `(${unreadCount}) ` : ''}Simple Rss`}</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main>
        <div
          className={`container-fluid text-center py-5 px-5 bg-dark text-white height-full`}
          style={{
            minHeight: windowHeight,
          }}>
          <h1>Welcome to Simple RSS</h1>
          <div className="test"></div>
          <h2>Username</h2>
          <input
            className="form-control"
            onChange={event => setUsername(event.target.value)}
            onKeyDown={e => e.key === 'Enter' && setPauseQueries(false)}
          />
          <SubmitButton
            className="col-sm-6 my-4"
            isLoading={isFetching}
            onClick={() => setPauseQueries(false)}>
            Submit
          </SubmitButton>

          {errorMessage ? <p>{errorMessage}</p> : null}
          <Row xs={1} md={2} lg={3} xl={4} className="g-4 text-dark">
            {items?.map(item => (
              <FeedCard key={item.id} item={item} onItemClick={onItemClick} />
            ))}
          </Row>
          {showFetchAll ? (
            <Button className="col-sm-2 my-3" onClick={() => setFetchAll(true)}>
              Get all items
            </Button>
          ) : null}
          <div />
          {showMarkAllRead ? (
            <Button className="col-sm-2 my-3" onClick={markAllRead}>
              Mark all as read
            </Button>
          ) : null}
        </div>
      </main>
    </div>
  );
};

export default Home;
